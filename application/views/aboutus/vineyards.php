<p>In addition to the grapes we grow, some other principal elements of sustainability that go into our wines a<a><img border="1" align="right" alt="" src="/coopergarrod/up/editor/image/Cooper_Garrod_Vineyard%20solar%20array.jpg" style="width: 165px; height: 124px;" vspace="3" hspace="3" /></a>re:</p>
<p><strong><a name="energy"></a>Energy </strong> approximately 25% of our total electricity use, including the stables, comes from the 17 Kw, 84-panel solar array that Akeena Solar installed in 2005. Click on the photo for current production and direction of flow.</p>
<p><strong>Glass </strong>- After it has preserved the quality of our wine until you can enjoy it, our primary concern is the recyclability of the glass container.&nbsp; When multi-stream recycling is used, e.g., the traditional 3 curbside bins, downstream glass recycling plants expect 98% recovery.&nbsp; When single stream is used, however, this drops to about 75% because of contamination of the glass cullet by other products in the recycling stream. Our primary bottle supplier is <a href="http://www.o-i.com/nth_us.aspx?id=1288">Owens-Illinois</a>.</p>
<p><strong>Birdboxes - </strong><a href="/coopergarrod/up/editor/file/raptor%20roosts%20with%20insert.jpg">click </a>for an example of raptor roosting and <a href="/coopergarrod/up/editor/file/BirdBoxMap_1_5_2012.pdf">here</a> for a map of our nest program.</p>
<p><strong>Cardboard</strong> - Used in our case boxes and shipping containers and inserts. Normally 100% recycable, but when our local recycling agency went to single stream recycling, we began to look for a means to return to multistream recycling. We found this by teaming with our local Safeway to accept our cardboard into their in-house recycling, where it is bundled and sold to a paper recycler.</p>
<p><strong>Corks </strong>- All natural from Portugal. We started buying corks from Juvenal Direct in the late 1990's, after they refined their Delfin process for uniform drying of the corks to minimize the TCA risk. Cork: Walk on the Wildlife Side.</p>
<p><strong>Capsules</strong> - Ours are tin and use water-based inks to eliminate acids in the manufacturing process. This process also creates the highest recycling value because we can return all the capsules from the bottles we open in the tasting room to Ramondin where they are remelted and made into new capsules.</p>
<p><strong>Pomace</strong> (skins, stems and seeds - what's left after pressing) - composted organically onsite and available for purchase. Pomace is also what one would ferment to make <em>grappa</em>.</p>
<p><strong>Lees</strong> (the particulates that settle at the bottom of tanks and barrels; we rack the wine to separate the wine from the lees) - a rich source of nitrogen for the soil, but not good if allowed to go into waters that lead to San Francisco Bay. As a result, we collect all tank lees (about 90% of what we produce) and add it to the compost pile to enrich our compost. What remains, primarily from rinsing barrels, flows to our septic system, not the Bay.</p>

