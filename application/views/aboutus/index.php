<p>The sunny days, moderate nights, and long growing season of the Santa Cruz Mountains, which are south of San Francisco and overlook the Pacific Ocean and the San Francisco Bay, have long been recognized as ideal for world class wines.</p>
<p>The Garrod family began involvement with California agriculture and open space in 1893, purchasing lands from the Mount Eden Orchard and Vineyard Company. Our label recalls glorious apricot and prune blossoms which enhanced spring views from our hillside early in the previous century. Since 1972, aging orchards have been replaced with grapes which thrive in this microclimate.</p>
<p>So continues the agricultural tradition begun by R.V. Garrod,  winemaker Bill Cooper and vineyard master Jan Garrod's grandfather. Winemaker Emeritus George Cooper refined his craft by learning from his fellow vintners in the Santa Cruz Mountains, Napa Valley, Burgundy, and Bordeaux.</p></p>
