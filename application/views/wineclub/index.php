                    <h1>Cooper-Garrod Estate Vineyards: Wine Club</h1>
                <p><div>Revised: May 25, 2016</div>
<p style="text-align: center">Join the Cooper-Garrod family, experience the legacy, share in the celebration, and explore our most precious wines throughout the year. We invite you to become a member of our wine club and savor the benefits and privileges of membership.</p>
<div><b>Benefits:</b></div>
<div></div>
<div><strong>Quarterly 3-bottle shipments</strong></div>
<ul>
    <li>20%discount onwine andmerchandise</li>
    <li>Complimentary tastings for wine club member and 3 guests</li>
    <li>Exclusive access to wine club member only wines and release parties</li>
    <li>Priority registrations and discounts for events open to the general public</li>
    <li>Early accessto verticals and large format bottles</li>
    <li>Invitations to wine-club only barrel tastings, winemaker seminars and dinners</li>
    <li>Membership referral program</li>
    <li>20% discount on horse trail rides for member and one guest</li>
    <li>20% discount on private riding lessons for member and family</li>
</ul>
<p><b>Quarterly 6-bottle shipments</b></p>
<ul>
    <li>Above, plus</li>
    <li>Two complimentary admissions to a wine tasting class per year</li>
</ul>
<div><b>Quarterly 12-bottle shipments</b></div>
<ul>
    <li>Above, plus</li>
    <li>We split the shipping cost on all wine club shipments</li>
</ul>
<div><strong>How the Club works:</strong></div>
<div></div>
<div>Membership provides you a choice of 3, 6 or 12-bottle quarterly shipments; order more, save on <a href="">shipping</a>. Thus, you will have the opportunity to sample wines, many of which are sold only at the winery, and we start you out with the current Club selection. Enroll <a href="">online</a> or complete, sign, and return <a href="pdf/CathedralClub-online.pdf">this form,</a> then sit back and enjoy Cooper-Garrod wines throughout the year!</div>
